int	invalid_input(int argc, char **argv)
{
	int	i;
	int	j;

	if (argc != 10)
		return (1);
	i = 1;
	while (i < argc)
	{
		j = 0;
		while (argv[i][j] != 0 || j != 9)
		{
			if (!((argv[i][j] >= '1' && argv[i][j] <= '9')
				|| argv[i][j] == '.'))
				return (2);
			if (++j > 9)
				return (3);
		}
		i++;
	}
	return (0);
}
