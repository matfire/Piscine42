void	duplicate_argv(char **argv, char array[9][9])
{
	int		i;
	int		j;

	i = 0;
	while (i < 9)
	{
		j = 0;
		while (j < 9)
		{
			array[i][j] = argv[i + 1][j];
			j++;
		}
		array[i][j] = '\0';
		i++;
	}
}
