int		validate_grid(char grid[9][9])
{
	int		i;
	int		j;

	j = 0;
	while (j < 9)
	{
		i = 0;
		while (i < 9)
		{
			if (grid[i][j] == '.')
				return (1);
			i++;
		}
		j++;
	}
	return (0);
}
