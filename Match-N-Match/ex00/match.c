int		match(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[i] != '\0')
	{
		if (s2[i] == s1[i] || (s2[i] == '*' && s2[i + 1] == s1[i + 1]))
			i++;
		return (1);
	}
	return (0);
}
