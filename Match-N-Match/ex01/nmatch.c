int		nmatch(char *s1, char *s2)
{
	int n;
	int i;

	n = 0;
	i = 0;
	while (s1[i] != '\0')
	{
		if (s2[i] == s1[i] || (s2[i] = "*" && s2[i + 1] == s1[i + 1]))
			n++;
		i++;
	}
	return (n);
}

int		main()
{
	char *s1;
	char *s2;

	s1 = "abcbd";
	s2 = "*b*";
	nmatch(s1, s2);
}
