int		ft_atoi(char *str)
{
	int i;
	int sign;
	int val;
	int nbr;

	i = 0;
	sign = 1;
	val = 0;
	nbr = 0;
	if (str[0] == '-')
	{
		sign = -1;
		str++;
	}
	while (str[i] >= '0' && str[i] <= '9' && str[i] != '\0')
	{
		nbr = (int)(str[i] - '0');
		val = (val * 10) + nbr;
		i++;
	}
	i++;
	return (val * sign);
}
